package com.yaneodo.jobnotice;

public class ApplyDTO {
	
	private String applySeq;
	private String jobOpeningSeq;
	private String applyStatusSeq;
	private String customerSeq;
	private String applyDate;
	
	
	public String getApplySeq() {
		return applySeq;
	}
	public void setApplySeq(String applySeq) {
		this.applySeq = applySeq;
	}
	public String getJobOpeningSeq() {
		return jobOpeningSeq;
	}
	public void setJobOpeningSeq(String jobOpeningSeq) {
		this.jobOpeningSeq = jobOpeningSeq;
	}
	public String getApplyStatusSeq() {
		return applyStatusSeq;
	}
	public void setApplyStatusSeq(String applyStatusSeq) {
		this.applyStatusSeq = applyStatusSeq;
	}
	public String getCustomerSeq() {
		return customerSeq;
	}
	public void setCustomerSeq(String customerSeq) {
		this.customerSeq = customerSeq;
	}
	public String getApplyDate() {
		return applyDate;
	}
	public void setApplyDate(String applyDate) {
		this.applyDate = applyDate;
	}
	
	

}
